const leitor = (numero) => {
    switch (numero) {
        case 0:
            console.log('zero')
            break
        case 1:
            console.log('um')
            break
        case 2:
            console.log('dois')
            break
        case 3:
            console.log('três')
            break
        case 4:
            console.log('quatro')
            break
        case 5:
            console.log('cinco')
            break
        case 6:
            console.log('seis')
            break
        case 7:
            console.log('sete')
            break
        case 8:
            console.log('oito')
            break
        case 9:
            console.log('nove')
            break
        case 10:
            console.log('dez')
            break
        default:
            console.log('Número fora do intervalo.')
    }
}

leitor(0)
leitor(1)
leitor(2)
leitor(3)
leitor(4)
leitor(5)
leitor(6)
leitor(7)
leitor(8)
leitor(9)
leitor(10)
leitor(55)