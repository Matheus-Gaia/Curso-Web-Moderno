let dobro = function (a) {
    return 2 * a
}
console.log(dobro(5))
console.log(dobro(Math.PI))


dobro2 = (a) => {
    return 2 * a
}
console.log(dobro2(5))
console.log(dobro2(Math.PI))

dobro3 = a => 2 * a
console.log(dobro3(5))
console.log(dobro3(Math.PI))

